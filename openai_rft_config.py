    task_id: str
    split: str
    model_version: str
    terminal_score: float
    ending_cash: float
    mean_final_soil: float
    ever_bankrupt: bool
    quarters_completed: int
    grader_variant: str

    def to_dict(self) -> Dict[str, Any]:
        return {
            "task_id": self.task_id,
            "split": self.split,
            "model_version": self.model_version,
            "terminal_score": self.terminal_score,
            "ending_cash": self.ending_cash,
            "mean_final_soil": self.mean_final_soil,
            "ever_bankrupt": self.ever_bankrupt,
            "quarters_completed": self.quarters_completed,
            "grader_variant": self.grader_variant,
        }
